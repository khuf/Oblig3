package no.uib.info233.v2017.khu010.oblig3;

public class Point {
	
	private final int pointA;
	private final int pointB;
	
	public Point(int pointA, int pointB) {
		this.pointA = pointA;
		this.pointB = pointB;
	}
	
	public int getPointA() { return pointA; }
	
	public int getPointB() { return pointB; }

}
